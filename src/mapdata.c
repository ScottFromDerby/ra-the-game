uint32_t get_meta_tile_info(MetaTile metaTile, uint8_t* TL, uint8_t* TR, uint8_t* BL, uint8_t* BR)
{
    switch(metaTile)
    {  
        case Empt:
        case IWal:
        *TL = 0x00;
        *TR = 0x00;
        *BL = 0x00;
        *BR = 0x00;
        return 0;

        case Gras:
        *TL = 0x3a;
        *TR = 0x00;
        *BL = 0x00;
        *BR = 0x3a;
        return 0;

        case Gra2:
        *TL = 0x00;
        *TR = 0x3a;
        *BL = 0x3a;
        *BR = 0x00;
        return 0;
        
        case TGrs:
        *TL = *TR = *BL = *BR = 0x55;
        return 0;

        case Path:
        *TL = *TR = *BL = *BR = 0x54;
        return 0;

        case HsWn:
        *TL = 0x0c;
        *TR = 0x0d;
        *BL = 0x1c;
        *BR = 0x1d;
        return 0;

        case HsDr:
        *TL = 0x0e;
        *TR = 0x0f;
        *BL = 0x1e;
        *BR = 0x1f;
        return 0;

        case RfTL:
        *TL = 0x66;
        *TR = 0x67;
        *BL = 0x69;
        *BR = 0x01;
        return 0;

        case RfTC:
        *TL = 0x67;
        *TR = 0x67;
        *BL = 0x01;
        *BR = 0x01;
        return 0;

        case RfTR:
        *TL = 0x67;
        *TR = 0x68;
        *BL = 0x01;
        *BR = 0x79;
        return 0;

        case RfBL:
        *TL = 0x69;
        *TR = 0x01;
        *BL = 0x76;
        *BR = 0x77;
        return 0;

        case RfBC:
        *TL = 0x01;
        *TR = 0x01;
        *BL = 0x77;
        *BR = 0x77;
        return 0;

        case RfBR:
        *TL = 0x01;
        *TR = 0x79;
        *BL = 0x77;
        *BR = 0x78;
        return 0;


        case Tre1:
        *TL = 0x4c;
        *TR = 0x4d;
        *BL = 0x5c;
        *BR = 0x5d;
        return 0;

        case Tre2:
        *TL = 0x4e;
        *TR = 0x4f;
        *BL = 0x5e;
        *BR = 0x5f;
        return 0;

        case Tre3:
        *TL = 0x04;
        *TR = 0x05;
        *BL = 0x14;
        *BR = 0x15;
        return 0;

        case Tre4:
        *TL = 0x06;
        *TR = 0x07;
        *BL = 0x16;
        *BR = 0x17;
        return 0;

        case Kenl:
        *TL = 0x3e;
        *TR = 0x3e;
        *BL = 0x3f;
        *BR = 0x3f;
        return 0;

        case Fenc:
        *TL = 0x6c;
        *TR = 0x6c;
        *BL = 0x7c;
        *BR = 0x7c;
        return 0;

        case Barl:
        *TL = 0x88;
        *TR = 0x89;
        *BL = 0x98;
        *BR = 0x99;
        return 0;

        default:
        *TL = 0x00;
        *TR = 0x01;
        *BL = 0x02;
        *BR = 0x03;
        return 0;
    }
}

